public class Print {
    public static void main(String[] args) {
        int a=10;
        int b=-20;
        System.out.printf("%1$05d",a,b);
        // format specifier
        // argument index - $1 $2 $3...
        // flag - '+', '-', '', '('
        // conversion
        // char c
        // int d o x
        // float f e g
        // String s
    }
}
