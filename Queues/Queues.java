package Queues;
import java.util.Queue;
import java.util.Deque;
import java.util.LinkedList;
import java.util.Deque;
import java.util.ArrayDeque;

public class Queues {
    public static void main(String[] args) {
        Queue<Integer> q = new LinkedList<>();
        Deque<Integer> d = new ArrayDeque<>();
        q.add(1);
        q.add(2);
        q.add(3);
        d.add(213);
        // Queue is a data structure in which we can add elements from one end and remove from other end
        // deque is a data structure in which we can add and remove elements from both the sides
    }
    

    
}
